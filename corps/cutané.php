Signe certain du vieillissement, mais aussi d’autres évènements de la vie comme une perte de poids importante ou une grossesse, le relâchement de la peau du corps finit par affecter tout le monde un jour ou l’autre. 
<br>
Le relâchement cutané est une distension due à une perte d'élasticité liée à l'âge. Le renouvellement cellulaire est ralenti, ce qui affaiblit les fibroblastes (cellules du derme qui fabriquent collagène et élastine). Le relâchement est d'abord musculaire puis cutané, entraînant un excès de peau et un affaissement.
<br>
La clinique des sacres vous propose différentes techniques permettant de lutter efficacement contre le relâchement cutanée sans anesthésie et chirurgie

