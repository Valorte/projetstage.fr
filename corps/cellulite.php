La prise de poids, les changements hormonaux ou encore le manque d’activité physique, favorisent l’apparition de la cellulite. Les amas graisseux situés sur les cuisses, le ventre ou encore les bras, donnent souvent un aspect peau d’orange
<br>
Cette cellulite est liée à une accumulation locale de graisses et à une modification du tissu adipeux dans certaines zones du corps.
<br>
Au sein de l’hypoderme, les cellules adipeuses, ou adipocytes, sont logées dans de petits compartiments. Si le nombre et le volume des adipocytes augmentent, ces petits compartiments gonflent. Ils prennent alors une forme bombée qui se répercute sur le derme et l’épiderme. La surface de la peau devient bosselée, d’où les expressions « peau d’orange » ou « capitons » (la peau prend l’aspect d’un tissus capitonné) fréquemment utilisées.
<br>
Considérée comme un phénomène physiologique normal, la cellulite est présente chez 90% des femmes et représente l’un des complexes les plus présents chez les français.


